first_number = input("Enter your price :")
second_number = input("Enter another price :")
numbera = int(first_number)
numberb = int(second_number)


if numbera > numberb :
    print("The first price is larger than the second one.")

elif numbera < numberb :
    print("The first price is smaller than the second one.")

elif numbera == numberb :
    print("The price are the same.")

